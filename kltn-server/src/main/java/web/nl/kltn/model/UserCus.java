package web.nl.kltn.model;

public class UserCus {
	private String userId;
	private String role;
	
	public UserCus() {}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}
	
}
